# Variables
## It is like a box which has been assigned a name w/ stuff inside and we can re use the variable

## Assignment of variable example
example_variable = 'Cats'

## Calling variable
print(example_variable)

## Re assign what is inside the box
example_variable = 'Dogs'
print(example_variable)

## Print statement --> Outputs to the console
## Print with type() tells us the data type within the print statement e.g int,str etc.

example_variable2 = 48
print(type(example_variable))
print(type(example_variable2))